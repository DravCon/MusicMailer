def _e(text: str) -> str:
    """HTML-escape a string for safe embedding."""
    return html.escape(str(text or ""), quote=False)


def _paragraphs(text: str) -> str:
    """Convert double-newline-separated paragraphs into <p> tags."""
    parts = [p.strip() for p in str(text or "").split("\n\n") if p.strip()]
    return "".join(
        f'<p style="margin:0 0 12px;font-size:15px;line-height:1.75;color:#374151;">{_e(p)}</p>'
        for p in parts
    )


