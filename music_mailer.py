# ---------------------------------------------------------------------------
# Step 3: Send the email
# ---------------------------------------------------------------------------

def send_email(html_content: str, subject: str) -> None:
    msg            = MIMEMultipart("alternative")
    msg["Subject"] = subject
    msg["From"]    = f"{SENDER_NAME} <{SMTP_USER}>"
    msg["To"]      = RECIPIENT_EMAIL

    # Plain-text fallback
    plain = f"{subject}\n\nOpen this email in an HTML-capable client to view the newsletter."
    msg.attach(MIMEText(plain, "plain"))
    msg.attach(MIMEText(html_content, "html", "utf-8"))

    log.info("Connecting to %s:%s…", SMTP_HOST, SMTP_PORT)
    with smtplib.SMTP(SMTP_HOST, SMTP_PORT) as server:
        server.ehlo()
        server.starttls()
        server.login(SMTP_USER, SMTP_PASSWORD)
        server.sendmail(SMTP_USER, RECIPIENT_EMAIL, msg.as_string())

    log.info("✓ Email sent to %s", RECIPIENT_EMAIL)


# ---------------------------------------------------------------------------
# Entry point
# ---------------------------------------------------------------------------

def _check_config() -> list[str]:
    required = ["ANTHROPIC_API_KEY", "SMTP_USER", "SMTP_PASSWORD", "RECIPIENT_EMAIL"]
    return [k for k in required if not os.getenv(k)]


def main() -> int:
    test_mode = "--test" in sys.argv   # print HTML to stdout, don't send

    log.info("═" * 60)
    log.info("Music Mailer starting%s", " (TEST MODE)" if test_mode else "")

    missing = _check_config()
    if missing:
        log.error("Missing required env vars: %s", ", ".join(missing))
        log.error("Copy .env.example → .env and fill in your values.")
        return 1

    try:
        log.info("Generating newsletter via Claude + web search…")
        data = generate_newsletter_content()

        log.info("Building HTML email…")
        html_content = build_html_email(data)

        today   = datetime.now().strftime("%B %d, %Y")
        subject = f"🎵 Music Daily — {today}"

        if test_mode:
            print(html_content)
            log.info("Test mode: HTML written to stdout. Not sending email.")
        else:
            send_email(html_content, subject)

        log.info("Done.")
        return 0

    except Exception:
        log.exception("Newsletter failed")
        return 1


if __name__ == "__main__":
    sys.exit(main())
